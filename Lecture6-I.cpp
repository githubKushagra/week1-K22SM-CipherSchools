#include <iostream>
using namespace std;

int main()
{

    // for loop syntax -  for(initialization , condition , updation) { code ;}

    // example for for-loop (table of 2).F
    for (int i = 0; i < 10; i++)
    {
        cout << "2 x " << i + 1 << " = " << 2 * (i + 1) << endl;
    }

    return 0;
}